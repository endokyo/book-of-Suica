package bean;

public class GenreBean {
	private int id; //ジャンルIDを保持
	private String genre; // ジャンル名を保持

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

}